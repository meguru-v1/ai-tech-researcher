


/** サイトの更新履歴（changelog）データ。
 *  - 一般の初見ユーザーが読んで「育っているサイト」と分かる粒度に、git履歴を人間語へ翻訳して手書きで保持する。
 *    コミット1件＝1エントリにはせず、地味・重複・後退的な修正は載せない。明らかな更新だけを残して詳しく書く。
 *  - バージョン番号は振らない（常時デプロイのWebサービスではSemVerが意味を持たないため日付ベース）。
 *    代わりに開発の「世代」をマイルストーン章として束ねる。世代表記は対外的な「版」として、公開初期=α版、
 *    以降を v1 から数える（内部の実装世代呼称より一段階手前。今は v5 世代＝ベータ版として公開中）。
 *  - 日付は表示そのままの 'YYYY.MM.DD' 文字列で持つ（タイムゾーン計算を挟まない）。
 */

export type ChangeCategory = '新機能' | '改善' | '修正' | 'プライバシー・規約';

export type ChangeEntry = {
  date: string; // 'YYYY.MM.DD'
  title: string;
  category: ChangeCategory;
  /** 明らかな更新のみ、何がどう良くなったかを1〜2文で補足する（任意）。 */
  detail?: string;
};

export type Milestone = {
  /** 一般向けの章タイトル */
  name: string;
  /** 対外的な版ラベル（小さく添える。公開初期=α版、以降 v1〜）。 */
  era: string;
  /** 期間表記 */
  period: string;
  /** その章の一言 */
  note: string;
  /** 新しい順に並べたエントリ */
  entries: ChangeEntry[];
};

