/** サイトの更新履歴（changelog）データ。
 *  - 一般の初見ユーザーが読んで「育っているサイト」と分かる粒度に、git履歴を人間語へ翻訳して手書きで保持する。
 *    実際の開発量をありのままに反映しつつ、純粋な内部リファクタ・重複・後退的修正は載せない。
 *  - バージョンは **セマンティックバージョニング** vMAJOR.MINOR.PATCH。番号の各桁に意味（重み）を持たせる:
 *      MAJOR … 世代の節目・大規模刷新（破壊的に近い大きな変化）。milestone:true。
 *      MINOR … 後方互換の「新機能」追加。（カテゴリ「新機能」。大きな能力追加もここ）
 *      PATCH … 後方互換の「改善・修正・規約/プライバシー更新」。（カテゴリ「改善」「修正」「プライバシー・規約」）
 *  - **α版（v1.0.0 より前の立ち上げ期）は1エントリにまとめる**（個別採番せず "α版" 表記）。以降は 1.x→2.x。
 *    対外ステージ: 公開初期=α版 → 自己完結エンジン(1.x) → 一般公開=ベータ版(2.x・現在)。
 *  - 日付は表示そのままの 'YYYY.MM.DD' 文字列で持つ（タイムゾーン計算を挟まない）。
 */

